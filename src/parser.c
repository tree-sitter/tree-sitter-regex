      if (lookahead == '=') ADVANCE(39);
      if (lookahead == '>') ADVANCE(58);
      if (lookahead == '?') ADVANCE(61);
      if (lookahead == '[') ADVANCE(45);
      if (lookahead == '\\') ADVANCE(10);
      if (lookahead == ']') ADVANCE(47);
      if (lookahead == '^') ADVANCE(34);
